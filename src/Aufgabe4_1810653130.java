public class Aufgabe4_1810653130 {

    public static void main(String[]args)
    {
        Integer zahl1 = 5;
        Integer zahl2 = 10;

        System.out.println(zahl1 < zahl2);
        System.out.println(zahl2 < zahl1);
        System.out.println(zahl1 > zahl2);
        System.out.println(zahl2 > zahl1);
        System.out.println(zahl1 <= zahl2);
        System.out.println(zahl2 <= zahl1);
        System.out.println(zahl1 >= zahl2);
        System.out.println(zahl2 >= zahl1);
        System.out.println(zahl1 == zahl2);
        System.out.println(zahl1 != zahl2);
    }
}
